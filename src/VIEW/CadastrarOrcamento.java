    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    try {
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            int selectedRowIndex = tabela.getSelectedRow();
            Connection conn;
            PreparedStatement pstm;
            tabela.getModel();
            conn = DriverManager.getConnection("jdbc:sqlite:banco/banco_oficina.db");
            String sql = " UPDATE tborcamentos SET cpf_Cliente = ?, cpf_Mecanico = ?, valor = ?, servicos = ? WHERE ID = ?";
            pstm = conn.prepareStatement(sql);
            pstm.setString(1, lblCpfC.getText());
            pstm.setString(2, lblCpfM.getText());
            pstm.setString(3, lblValor.getText());
            pstm.setString(4, lblServicos.getText());
            pstm.setString(5, model.getValueAt(selectedRowIndex, 0).toString());
            JOptionPane.showMessageDialog(null, "Atualizado");
            pstm.execute();
            atualizarTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        DefaultTableModel dtmTabela = (DefaultTableModel) tabela.getModel();
        try {
            Connection con;
            Statement st;
            con = DriverManager.getConnection("jdbc:sqlite:banco/banco_oficina.db");
            st = con.createStatement();
            st.executeUpdate("insert into tborcamentos(cpf_Cliente,cpf_Mecanico,valor,servicos) values('" + lblCpfC.getText() + "','" + lblCpfM.getText() + "','" + lblValor.getText() + "','"+ lblServicos.getText()+"')");
            JOptionPane.showMessageDialog(null,"Inseridos");
            limparCampos();
            lblCpfC.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        atualizarTabela();
        
    }//GEN-LAST:event_btnAdicionarActionPerformed

