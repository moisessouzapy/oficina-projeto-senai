public void mouse(){
    DefaultTableModel dtmTabela = (DefaultTableModel) tabela.getModel();
        int selectedRowIndex = tabela.getSelectedRow();
        
        lblCpfC.setText(dtmTabela.getValueAt(selectedRowIndex, 1).toString());
        lblCpfM.setText(dtmTabela.getValueAt(selectedRowIndex, 2).toString());
        lblValor.setText(dtmTabela.getValueAt(selectedRowIndex, 3).toString());
        lblServicos.setText(dtmTabela.getValueAt(selectedRowIndex, 4).toString());
}
    public void atualizarTabela() {
        DefaultTableModel dtmTabela = (DefaultTableModel) tabela.getModel();
        List<Pessoa> lista = new ArrayList();
        dtmTabela.setRowCount(0);
        try {
            Connection con;
            PreparedStatement st;
            con = DriverManager.getConnection("jdbc:sqlite:banco/banco_oficina.db");
            String sql = "select * from tborcamentos";
            st = con.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            lista.clear();
            while(rs.next()){
                Pessoa pessoa = new Pessoa();
                pessoa.setID(rs.getInt("ID"));
                pessoa.setCpf_Cliente(rs.getString("cpf_Cliente"));
                pessoa.setCpf_Mecanico(rs.getString("cpf_Mecanico"));
                pessoa.setValor(rs.getString("valor"));
                pessoa.setServicos(rs.getString("servicos"));
                lista.add(pessoa);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } 
       
        Pessoa pessoa = new Pessoa();
        for(Pessoa p:lista){
        Object[] dados = {p.getID(),p.getCpf_Cliente(),p.getCpf_Mecanico(),p.getValor(),p.getServicos()};
        dtmTabela.addRow(dados);
        }
        
}
private void limparCampos(){
            lblCpfC.setText("");
            lblCpfM.setText("");
            lblValor.setText("");
            lblServicos.setText("");
            lblCpfC.requestFocus();
    }
